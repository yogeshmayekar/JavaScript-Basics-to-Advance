// Primitive data type 
let number1 = 34;
let number2 = number1;
console.log("value of the number 1 is:", number1);
console.log("value of the number 2 is:", number2);

number1 = 45;
console.log("value of the number 1 after update is:", number1);
console.log("value of the number 2 after update is:", number2);

// another example 
let str1 = "Yogesh";
